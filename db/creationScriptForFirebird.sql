ALTER PROCEDURE UPDATE_SIG_WORD (
    GRP_CNCPT Integer,
    WORD Varchar(100),
    ADDED_RELEVANCE Float,
    WITH_INSERT Char(1) )
RETURNS (
    ADDED_RECORDS Integer,
    UPDATE_DRECORDS Integer )
AS
DECLARE VARIABLE ID integer;
    BEGIN
        ID = null;
        ADDED_RECORDS = 0;
        UPDATE_DRECORDS = 0;
        select ID from GRP_CNCPT_WORDS where GRP_CNCPT = :GRP_CNCPT and WORD = :WORD  into :ID;
        IF(ID is not null) THEN
        BEGIN
            update GRP_CNCPT_WORDS  set RELEVANCE = RELEVANCE + :ADDED_RELEVANCE
            where GRP_CNCPT = :GRP_CNCPT and WORD = :WORD;
            UPDATE_DRECORDS = 1;
        END
        else if(WITH_INSERT = '1') then
        BEGIN
            insert into GRP_CNCPT_WORDS(GRP_CNCPT, "WORD", RELEVANCE) values (:GRP_CNCPT, :WORD, :ADDED_RELEVANCE);
            ADDED_RECORDS = 1;
        END
    END^
SET TERM ; ^

