ALTER PROCEDURE INSER_ASS_FILE_SIGWORDS (
    GROUPID Integer,
    FILEID Integer,
    SIGWORD Varchar(100) )
RETURNS (
    NRECORD Integer )
AS
DECLARE VARIABLE wordID INTEGER;

BEGIN
    select GCW.ID from GRP_CNCPT_WORDS GCW
    where GCW.GRP_CNCPT = :groupID and GCW.WORD = :sigWord and GCW.ID not in (
        select FSW.WORD_ID from FILE_SIG_WORDS FSW
        where FSW.FILE_ID = :fileID and FSW.WORD_ID = GCW.ID ) into :wordID;

    if(wordID is not NULL) THEN
        BEGIN
        insert into FILE_SIG_WORDS(FILE_ID, WORD_ID)
        values(:fileID, :wordID);
        NRECORD = 1;
        END
    else NRECORD = 0;
END^
SET TERM ; ^

